                    <br>
                    <br>
                    En conclusión, se afirma que la deontología es aquella ciencia que se encarga de dictaminar que es lo correcto e incorrecto en cada profesión. Su finalidad es definir las normas a seguir en el ejercicio profesional, para obtener los mejores resultados posibles sin desfavorecer; tanto a las personas que se benefician del servicio, como al experto que lo ofrece.
