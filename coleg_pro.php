                    “Un colegio profesional es una asociación de carácter profesional, compuesta por unos integrantes que ejercen una misma profesión, siendo estos, habitualmente, pertenecientes a profesiones regladas o reconocidas por las normas que aplica el Estado”. Coll Morales, F. (2020). Los cuales “(…) se crearon para promover el desarrollo integral de sus miembros, cooperar con su formación, con su bienestar y sobrevivencia, promover relaciones armónicas y de cooperación, defender sus derechos, no solo profesionales sino, incluso, humanos”. Araya Vega (2020). Kömmerling (2011) afirma que “El colegio debe velar por el cumplimiento de una buena labor profesional, donde la práctica ética del trabajo se constituye como uno de los principios comunes que ayudan a definir los estatutos de cada corporación”.
                    <br>
                    <br>
                    En definitiva, un colegio de profesionales es una institución reconocida por el estado encargada de crear los códigos éticos por los cuales se van a guiar los profesionales afiliados a dicho colegio. Esto lo logran utilizando la deontología profesional, para designar que comportamientos se desean en las personas inscritas en dicha entidad. Todo esto realizado con la intensión de garantizar el buen desenvolvimiento de cada persona que se relacione directa o indirectamente con la institución.
