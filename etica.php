                    Existen multitud de puntos de vista sobre el concepto de ética, pero en lo que todos concuerdan es que trata sobre evaluar el comportamiento humano para designar que es correcto o no, por lo tanto, “La ética la definimos entonces como el conjunto de normas que tienen por objeto salvaguardar, fortalecer y preservar la vida de los individuos corpóreos”. Bueno, G. (2009). Para Singer, P. (2004) “La ética versa sobre los valores, sobre el bien y el mal, lo correcto y lo incorrecto: no podemos evitar implicarnos en ella, pues todo lo que hacemos —y dejamos de hacer— siempre es posible objeto de evaluación ética”. Entonces se dice que, “Su fin no es explicar la acción sino evaluarla, ya sea para justificar o para condenar lo que hemos hecho o lo que tenemos la intención de hacer”. Gómez Lobo, A. (1994).
