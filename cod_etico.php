                    “Se dice que código de ética profesional es un conjunto de valores morales que regulan conductas específicas en un entorno determinado”. Torres Hernández, Z. (2014). Estos “Contienen enunciados de valores y principios referidos al propósito de la institución, a sus obligaciones y responsabilidades y a las normas que deberán conformar el comportamiento de sus integrantes”. Torres Aramijos, E. (2014). Para Castillo Briceño, C. (2010) “Los códigos de ética cumplen la función de promover en los gremios profesionales la reflexión cotidiana sobre su quehacer, sobre los dilemas éticos que experimentan para autoimponerse las directrices que consideren coherentes con los valores éticos, los derechos fundamentales y principios constitucionales, así como la sana convivencia social”.
                    <br>
                    <br>
                    Por ende, los códigos de ética son la recopilación de todos los valores y normas que debe tener una persona en una determinada situación. Estos existen tanto para definir como es el buen comportamiento de un individuo en su día a día, como para establecer el comportamiento de un profesional ante ciertas situaciones. Todo esto se logra a través de la introspección evaluando cada acción tomada y decidiendo si es éticamente correcto realizarla.
